def persegi (sisi):
    luas_persegi= sisi*sisi
    keliling_persegi = 4*sisi
    print("jadi, hasil luas persegi adalah", luas_persegi)
    print("jadi, hasil keliling persegi adalah", keliling_persegi)


def segitiga(sisi,alas,tinggi):
    luas_segitiga = 1/2 * alas* tinggi
    keliling_segitiga = 3*sisi
    print("jadi, hasil luas segitiga adalah", luas_segitiga)
    print("Jadi, hasil keliling segitiga adalah", keliling_segitiga)

def belahketupat(d1,d2,sisi):
    luas_belahketupat = 1/2 * d1 * d2
    keliling_belahketpat = 4 * sisi
    print("Jadi, hasil luas belahketupat adalah", luas_belahketupat)
    print("Jadi, hasil keliling belahketupat adalah", keliling_belahketpat)

def jajargenjang(alas,tinggi,sisi):
    luas_jajargenjang = alas * tinggi
    keliling_jajargenjang = 2 * (alas + sisi)
    print("Jadi, hasil luas jajargenjang adalah", luas_jajargenjang)
    print("Jadi, hasil keliling jajargenjang adalah", keliling_jajargenjang)

def persegipanjang(panjang,lebar):
    luas_persegipanjang = panjang * lebar
    keliling_persegipanjang = 2, (panjang * lebar)
    print("jadi, hasil luas persegipanjang adalah", luas_persegipanjang)
    print("jadi, hasil keliling persegipanjang adalah", keliling_persegipanjang)

